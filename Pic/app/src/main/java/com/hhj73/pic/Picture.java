package com.hhj73.pic;

import java.util.Date;

public class Picture { // 사진
    String name; // 이름
    String text; // 추출한 text
    Date date;

    public Picture(String name, Date date) {
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}
